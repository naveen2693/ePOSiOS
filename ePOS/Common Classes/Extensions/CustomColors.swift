import UIKit

extension UIColor {
    
    class func darkThemeColor() -> UIColor {
        return UIColor.init(named: "themeDarkColor")!
    }
    
    class func lightThemeColor() -> UIColor {
        return UIColor.init(named: "themeLightColor")!
    }
    
    class func themeTextColor() -> UIColor {
        return UIColor.init(named: "themeLightColor")!
    }
    
    class func themeButtonBGColor() -> UIColor {
        return UIColor.init(named: "themeDarkColor")!
    }
    
    class func titleColor() -> UIColor {
        return UIColor.init(named: "titleColor")!
    }
    
    class func textColor() -> UIColor {
        return UIColor.init(named: "textColor")!
    }
    
    class func bigTitleColor() -> UIColor {
        return UIColor.init(named: "bigTitleColor")!
    }
    
    class func redButtonBGColor() -> UIColor {
        return UIColor.init(named: "redButtonBGColor")!
    }
    
    class func redTextColor() -> UIColor {
        return UIColor.init(named: "redTextColor")!
    }
    
    class func lightBorderColor() -> UIColor {
        return UIColor.init(named: "lightBorderColor")!
    }
    
    class func subTextColor() -> UIColor {
        return UIColor.init(named: "subTextColor")!
    }
    
    class func textBoxBorderColor() -> UIColor {
        return UIColor.init(named: "textBoxBorderColor")!
    }
}
